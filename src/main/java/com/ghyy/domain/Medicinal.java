package com.ghyy.domain;

public class Medicinal {
	private int id;
	private String spbh;
	private String name;
	private String shpchd;
	private String shpgg;
	private String dw;
	private String pizhwh;
	private String spid;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getSpbh() {
		return spbh;
	}

	public void setSpbh(String spbh) {
		this.spbh = spbh;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getShpchd() {
		return shpchd;
	}

	public void setShpchd(String shpchd) {
		this.shpchd = shpchd;
	}

	public String getShpgg() {
		return shpgg;
	}

	public void setShpgg(String shpgg) {
		this.shpgg = shpgg;
	}

	public String getDw() {
		return dw;
	}

	public void setDw(String dw) {
		this.dw = dw;
	}

	public String getPizhwh() {
		return pizhwh;
	}

	public void setPizhwh(String pizhwh) {
		this.pizhwh = pizhwh;
	}

	public String getSpid() {
		return spid;
	}

	public void setSpid(String spid) {
		this.spid = spid;
	}

}
